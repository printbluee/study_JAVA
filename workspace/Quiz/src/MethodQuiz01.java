
public class MethodQuiz01 {

	/*
	 * [ 메소드 정의 ]
	 * - 기능 : 양의 정수 2개를 받아, 누적합 출력
	 * - 메서드명 : add, 매개변수 : 2개, 리턴값 : 없음
	 * 
	 * < 출력 형태 >
	 * 2 ~ 5 누적합 >> 14
	 */
	
	public static void main(String[] args) {
		
		// [ 메소드 호출 ]
		add(2, 5); // 2 + 3 + 4 + 5 = 14
		add(5, 2); // 2 + 3 + 4 + 5 = 14
		add(5, 1);
	}
}
