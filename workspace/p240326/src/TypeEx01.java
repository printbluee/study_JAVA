
public class TypeEx01 {
	
	public static void main(String[] args) {
		// sysout -> System.out.println(); 자동완성
		// 변수 : 메모리 공간에 부여한 이름, 이름을 통해서 메모리 접근
		
		// 변수 선언 : ;
		byte num;
		
		// 변수 초기화
		num = 5; 
		System.out.println(num);
		//이후 다시 선언할 수 x
		
		// 변수 선언(자료형 + 변수명;) & 초기화 -> 권장
		int var = 7;
		System.out.println(var);	
	}
}