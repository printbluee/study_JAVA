
public class MethodEx03 {
	
	// [ main method ] ================
	public static void main(String[] args) {

		showString();
		
	}
	
	// [ method 정의 ] ================
	// [ 형태 3 ] 매개변수 없고, 리턴 없음
	// 기능 : 호출하면 문자열 출력
	// 메소드명 : showString, 매개변수 : x
	// 반환할 것이 없어서 void 
	
	public static void showString() {
		
		System.out.println("--------------------------");
		System.out.println("showString() 메소드 실행됨");
		System.out.println("--------------------------");
	
	}
	
}

