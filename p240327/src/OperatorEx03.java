
public class operatorEx03 {

	public static void main(String[] args) {
		
		int n1 = 5;
		int n2 = 3;
		
		System.out.println("크니 ? " + (n1 > n2));
		
		// 변수에 집어넣기
		boolean result = n1 > n2;
		System.out.println("result ==> " + result);
		
		System.out.println("다르냐 ? " + (n1 != n2));
		System.out.println("같냐 ? " + (n1 == n2));
		
	}
}
