
public class CastingEx01 {

	public static void main(String[] args) {
		
		// [ 형변환 ] --> 타입 변환
		// 강제 형변환, 명시적 형변환	
		// 자동 형변환, 묵시적 형변환
		
		int n1 = 9;
		int n2 = 2;
		System.out.println("n1/n2 => " + n1/n2);
		System.out.println("n1/(float)n2 => " + n1/(float)n2);		
		System.out.println("(float)n1/n2 => " + (float)n1/n2);
		System.out.println("(float)(n1/n2) => " + (float)(n1/n2));
		
	}

}
